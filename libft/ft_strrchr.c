#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int	n;

	n = ft_strlen(s);
	while (n >= 0)
	{
		if (s[n] == (char)c)
		{
			return ((char *)&s[n]);
		}
		n--;
	}
	return (NULL);
}
