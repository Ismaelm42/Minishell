#include "libft.h"

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] != '\0' && s2[i] != '\0' && i <= (n - 1))
	{
		if (s1[i] == s2[i])
			i++;
		else if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
	}
	if (s1[i] == '\0' && i < n)
		return ((unsigned char)s2[i] * -1);
	if (s2[i] == '\0' && i < n)
		return ((unsigned char)s1[i]);
	else
		return (0);
}
